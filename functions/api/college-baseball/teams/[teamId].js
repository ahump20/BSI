// Get current season info
function getCurrentSeason() {
  const now = new Date();
  const month = now.getMonth() + 1;

  // Baseball season: Feb-June
  // If we're in Nov-Jan, show next year's preseason
  if (month >= 11 || month <= 1) {
    return {
      year: now.getFullYear() + 1,
      phase: 'preseason',
      label: `${now.getFullYear() + 1} Preseason`,
    };
  } else if (month >= 2 && month <= 6) {
    return { year: now.getFullYear(), phase: 'regular', label: `${now.getFullYear()} Season` };
  } else {
    return {
      year: now.getFullYear() + 1,
      phase: 'preseason',
      label: `${now.getFullYear() + 1} Preseason`,
    };
  }
}

// Fetch live team data from ESPN
async function fetchESPNTeamData(espnId) {
  try {
    const response = await fetch(`${ESPN_BASE}/teams/${espnId}`, {
      headers: { 'User-Agent': USER_AGENT },
    });

    if (!response.ok) return null;

    const data = await response.json();
    return data.team || null;
  } catch (error) {
    console.error('ESPN team fetch error:', error.message);
    return null;
  }
}

// Fetch team schedule from ESPN
async function fetchTeamSchedule(espnId) {
  try {
    const response = await fetch(`${ESPN_BASE}/teams/${espnId}/schedule`, {
      headers: { 'User-Agent': USER_AGENT },
    });

    if (!response.ok) return [];

    const data = await response.json();
    const events = data.events || [];

    return events.slice(0, 20).map((event) => {
      const competition = event.competitions?.[0];
      const opponent = competition?.competitors?.find((c) => c.team?.id !== String(espnId));
      const isHome =
        competition?.competitors?.find((c) => c.team?.id === String(espnId))?.homeAway === 'home';

      return {
        id: event.id,
        date: event.date,
        opponent: opponent?.team?.displayName || 'TBD',
        opponentLogo: opponent?.team?.logos?.[0]?.href,
        location: isHome ? 'Home' : 'Away',
        time: new Date(event.date).toLocaleString('en-US', {
          timeZone: 'America/Chicago',
          weekday: 'short',
          month: 'short',
          day: 'numeric',
          hour: 'numeric',
          minute: '2-digit',
        }),
        conference: opponent?.team?.conferenceId === 23 ? 'SEC' : 'Non-Conf',
        result:
          competition?.status?.type?.name === 'STATUS_FINAL'
            ? competition.competitors.find((c) => c.team?.id === String(espnId))?.winner
              ? 'W'
              : 'L'
            : null,
      };
    });
  } catch (error) {
    console.error('ESPN schedule fetch error:', error.message);
    return [];
  }
}

export async function onRequest(context) {
  const { request, env, params } = context;

  // Handle CORS
  if (request.method === 'OPTIONS') {
    return new Response(null, {
      headers: {
        'Access-Control-Allow-Origin': '*',
        'Access-Control-Allow-Methods': 'GET, OPTIONS',
        'Access-Control-Allow-Headers': 'Content-Type',
      },
    });
  }

  const teamId = params.teamId?.toLowerCase();

  // Get team from database
  const team = SEC_TEAMS[teamId];

  if (!team) {
    return new Response(
      JSON.stringify({
        success: false,
        error: 'Team not found',
        availableTeams: Object.keys(SEC_TEAMS),
      }),
      {
        status: 404,
        headers: {
          'Content-Type': 'application/json',
          'Access-Control-Allow-Origin': '*',
        },
      }
    );
  }

  // Fetch live data from ESPN
  const [espnData, schedule] = await Promise.all([
    fetchESPNTeamData(team.id),
    fetchTeamSchedule(team.id),
  ]);

  // Merge live data with static preseason data
  const enrichedTeam = {
    ...team,
    logo: espnData?.logos?.[0]?.href || `https://a.espncdn.com/i/teamlogos/ncaa/500/${team.id}.png`,
    record: espnData?.record?.items?.[0]?.summary || '0-0',
    conferenceRecord: espnData?.record?.items?.find((r) => r.type === 'vsconf')?.summary || '0-0',
    schedule: schedule.length > 0 ? schedule : generatePreseasonSchedule(team),
    liveData: espnData ? true : false,
  };

  const season = getCurrentSeason();
  const timestamp = new Date().toLocaleString('en-US', {
    timeZone: 'America/Chicago',
    year: 'numeric',
    month: '2-digit',
    day: '2-digit',
    hour: '2-digit',
    minute: '2-digit',
    hour12: true,
  });

  return new Response(
    JSON.stringify({
      success: true,
      team: enrichedTeam,
      season,
      meta: {
        fetchedAt: timestamp,
        timezone: 'America/Chicago',
        source: espnData ? 'ESPN API + BSI Database' : 'BSI Database',
        liveDataAvailable: !!espnData,
      },
    }),
    {
      headers: {
        'Content-Type': 'application/json',
        'Access-Control-Allow-Origin': '*',
        'Cache-Control': 'public, max-age=300',
      },
    }
  );
}

