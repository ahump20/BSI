#!/usr/bin/env bash
set -euo pipefail

# init-artifact.sh — Scaffolds a React 18 + TypeScript + Vite + Tailwind + shadcn/ui project
# Usage: bash init-artifact.sh <project-name> [target-dir]
#
# Creates a self-contained project ready for development and bundling.
# Default target directory: ~/tmp/<project-name>

PROJECT_NAME="${1:?Usage: init-artifact.sh <project-name> [target-dir]}"
TARGET_DIR="${2:-$HOME/tmp/$PROJECT_NAME}"

if [ -d "$TARGET_DIR" ]; then
  echo "ERROR: Directory already exists: $TARGET_DIR"
  echo "Remove it first or choose a different name."
  exit 1
fi

echo "==> Creating artifact project: $PROJECT_NAME"
echo "    Location: $TARGET_DIR"

# Check Node version
NODE_VERSION=$(node -v 2>/dev/null | sed 's/v//' | cut -d. -f1)
if [ -z "$NODE_VERSION" ] || [ "$NODE_VERSION" -lt 18 ]; then
  echo "ERROR: Node 18+ required. Current: $(node -v 2>/dev/null || echo 'not found')"
  exit 1
fi

# Scaffold with Vite
npm create vite@latest "$TARGET_DIR" -- --template react-ts
cd "$TARGET_DIR"

# Install core dependencies
npm install
npm install -D tailwindcss@3 postcss autoprefixer
npm install class-variance-authority clsx tailwind-merge lucide-react
npm install @radix-ui/react-slot

# Initialize Tailwind
npx tailwindcss init -p

# Configure Tailwind for shadcn/ui compatibility
cat > tailwind.config.js << 'TAILWIND_EOF'
